package operadores;

public class asignacion {
    public static void main(String[] args) {

        // asignacion
        int c = 10;

        // asignacion compuesta
        c += 5; // c = c + 5
        c -= 3; // c = c - 3
        c *= 2; // c = c * 2
        c /= 4; // c = c / 4
        c %= 3; // c = c % 3
        System.out.println(c);
    }
}
