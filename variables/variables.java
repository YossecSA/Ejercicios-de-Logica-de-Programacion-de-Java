package variables;

public class variables {
    public static void main(String[] args) {

        /*
         * Tipo de Variables
         * Variables Primitivas: Almacena valores basicos
         * variables de referencia: Almacena direcciones de memoria que apunta a objetos
         */

        // Primitivos

        // Enteros
        int numero = 129;

        // Flotantes
        float flotantes = 1.25f;
        double decimales = 5.01234567899;

        // Booleanos
        boolean verdadero = true;
        boolean falso = false;

        // Caracteres
        char letra = 'A';

        // Referencia
        String cadena = "Hola mundo";

        // Llamada de las variables
        System.out.println(numero);
        System.out.println(flotantes);
        System.out.println(decimales);
        System.out.println(verdadero);
        System.out.println(falso);
        System.out.println(letra);
        System.out.println(cadena);
    }
}
